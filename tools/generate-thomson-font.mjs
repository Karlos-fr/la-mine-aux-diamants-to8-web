#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";

const SOURCE_DIR = "C:/Users/qjsf8838/Downloads/thomson-8-bit (2)";
const SOURCE_GLYPHS = join(SOURCE_DIR, "thomson-8-bit.glyphs");
const SOURCE_PNG = join(SOURCE_DIR, "thomson-8-bit.png");
const GENERATED_TS = "src/assets/generated/thomson-8-bit-font.ts";
const GENERATED_MJS = "tools/generated-thomson-8-bit-font.mjs";
const DOCS_DIR = "docs/extraction/fonts";
const DOCS_PNG = join(DOCS_DIR, "thomson-8-bit-specimen.png");
const GLYPH_WIDTH = 8;
const GLYPH_HEIGHT = 8;
const FONTSTRUCT_GRID_SIZE = 227;
const FONTSTRUCT_LEFT_X = 340.5;
const FONTSTRUCT_TOP_Y = 1475.5;

main();

function main() {
  const glyphs = extractGlyphsFromGlyphsSource(readFileSync(SOURCE_GLYPHS, "utf8"));
  glyphs[" "] = Array.from({ length: GLYPH_HEIGHT }, () => "00000000");

  mkdirSync(resolve("src/assets/generated"), { recursive: true });
  mkdirSync(DOCS_DIR, { recursive: true });
  writeFileSync(GENERATED_TS, renderTypeScript(glyphs));
  writeFileSync(GENERATED_MJS, renderJavaScript(glyphs));
  writeFileSync(DOCS_PNG, readFileSync(SOURCE_PNG));

  if (existsSync("src/assets/fonts")) rmSync("src/assets/fonts", { recursive: true, force: true });
  console.log(`Generated ${Object.keys(glyphs).length} Thomson glyphs to ${GENERATED_TS}`);
}

function extractGlyphsFromGlyphsSource(source) {
  const glyphs = {};
  const glyphBlocks = source.matchAll(
    /glyphname = [^;]+;\s*unicode = ([0-9A-F]+);([\s\S]*?)(?=\n\{\nglyphname = |\n\);\n\);\n$)/g
  );

  for (const match of glyphBlocks) {
    const character = String.fromCodePoint(Number.parseInt(match[1], 16));
    const pixelPositions = [...match[2].matchAll(/transform = "\{[^,]+,[^,]+,[^,]+,[^,]+,([^,]+),([^}]+)\}"/g)]
      .map((pixelMatch) => toGridPosition(Number.parseFloat(pixelMatch[1]), Number.parseFloat(pixelMatch[2])));
    glyphs[character] = renderGlyphRows(pixelPositions);
  }

  return glyphs;
}

function toGridPosition(x, y) {
  return {
    column: Math.round((x - FONTSTRUCT_LEFT_X) / FONTSTRUCT_GRID_SIZE),
    row: Math.round((FONTSTRUCT_TOP_Y - y) / FONTSTRUCT_GRID_SIZE)
  };
}

function renderGlyphRows(pixelPositions) {
  const minColumn = Math.min(0, ...pixelPositions.map((pixel) => pixel.column));
  const rows = Array.from({ length: GLYPH_HEIGHT }, () => Array.from({ length: GLYPH_WIDTH }, () => "0"));
  for (const pixel of pixelPositions) {
    const column = pixel.column - minColumn;
    if (column < 0 || column >= GLYPH_WIDTH || pixel.row < 0 || pixel.row >= GLYPH_HEIGHT) {
      continue;
    }

    rows[pixel.row][column] = "1";
  }

  return rows.map((row) => row.join(""));
}

function renderTypeScript(glyphs) {
  const sorted = Object.fromEntries(Object.entries(glyphs).sort(([a], [b]) => a.localeCompare(b)));
  return `// Generated by tools/generate-thomson-font.mjs from Patrick H. Lauke's Thomson 8-bit FontStruct source.\n` +
    `// Source: ${SOURCE_GLYPHS}\n` +
    `// License: Creative Commons Attribution 3.0.\n` +
    `export const THOMSON_8_BIT_FONT = ${JSON.stringify({ width: GLYPH_WIDTH, height: GLYPH_HEIGHT, glyphs: sorted }, null, 2)} as const;\n` +
    `export type Thomson8BitFont = typeof THOMSON_8_BIT_FONT;\n`;
}

function renderJavaScript(glyphs) {
  const sorted = Object.fromEntries(Object.entries(glyphs).sort(([a], [b]) => a.localeCompare(b)));
  return `// Generated by tools/generate-thomson-font.mjs from Patrick H. Lauke's Thomson 8-bit FontStruct source.\n` +
    `// Source: ${SOURCE_GLYPHS}\n` +
    `// License: Creative Commons Attribution 3.0.\n` +
    `export const THOMSON_8_BIT_FONT = ${JSON.stringify({ width: GLYPH_WIDTH, height: GLYPH_HEIGHT, glyphs: sorted }, null, 2)};\n`;
}
