function readPointerTable(memory) {
  const pointers = [];
  for (let index = 0; index < LEVEL_COUNT; index += 1) {
    pointers.push(readWord(memory, POINTER_TABLE_ADDRESS + index * 2));
  }
  return pointers;
}

function decodeLevel(memory, address, levelIndex) {
  const header = Array.from(memory.subarray(address, address + HEADER_SIZE));
  const headerMeta = decodeHeader(header);
  const playerStart = headerPosition(header, 4);
  const exit = headerPosition(header, 6);
  const playerPosition = {
    x: playerStart.x,
    y: playerStart.y,
    gridAddress: playerStart.gridAddress
  };
  const exitPosition = {
    x: exit.x,
    y: exit.y,
    gridAddress: exit.gridAddress
  };
  const grid = Array.from({ length: HEIGHT }, () => Array(WIDTH).fill(null));
  const fullGrid = Array.from({ length: HEIGHT }, () => Array(STRIDE).fill(0x04));
  const counts = {};
  const specialMarkers = [];
  const tilePositions = {
    diamonds: [],
    rocks: [],
    walls: [],
    exits: [],
    monsters: []
  };
  let offset = address + HEADER_SIZE;
  let x = 0;
  let y = 0;

  while (y < HEIGHT) {
    const byte = memory[offset++];
    if (byte > 0x7f) {
      const tileId = byte & 0x7f;
      const count = memory[offset++];
      for (let repeat = 0; repeat < count; repeat += 1) {
        writeDecodedTile(fullGrid, grid, counts, specialMarkers, tilePositions, tileId, x, y);
        x += 1;
        if (x === WIDTH) {
          x = 0;
          y += 1;
          if (y === HEIGHT) break;
        }
      }
    } else {
      writeDecodedTile(fullGrid, grid, counts, specialMarkers, tilePositions, byte, x, y);
      x += 1;
      if (x === WIDTH) {
        x = 0;
        y += 1;
      }
    }
  }

  return {
    levelIndex,
    levelNumber: levelIndex + 1,
    sourceAddress: hexAddress(address),
    sourceOffset: hexAddress(address),
    header: header.map((value) => `0x${hex(value, 2)}`),
    headerMeta,
    headerSha256: sha256(Buffer.from(header)),
    encodedLength: offset - address,
    width: WIDTH,
    height: HEIGHT,
    stride: STRIDE,
    playerStart: playerPosition,
    exit,
    exitGridAddress: exit.gridAddress,
    headerPositions: {
      player: playerPosition,
      exit: exitPosition
    },
    tilePositions,
    counts: sortCounts(counts),
    specialMarkers,
    tileClassification: summarizeTilePositions(tilePositions),
    rows: grid.map((row) => row.map((tileId) => hex(tileId, 2)).join(" ")),
    grid
  };
}

function decodeHeader(header) {
  const rawStart = headerPosition(header, 4);
  const rawExit = headerPosition(header, 6);
  const timeRaw = readWordFromBytes(header, 8);
  const time = decodeBcdBytes(header[8], header[9]);
  const flags = {
    byte0: header[0],
    byte1: header[1],
    reserved16: readWordFromBytes(header, 0),
    reserved28: readWordFromBytes(header, 2),
    rawMode: header[10],
    param: header[11]
  };
  return {
    raw: header.map((value) => `0x${hex(value, 2)}`),
    player: {
      x: rawStart.x,
      y: rawStart.y,
      source: {
        byteOffset: 4,
        raw: [
          `0x${hex(header[4], 2)}`,
          `0x${hex(header[5], 2)}`
        ]
      }
    },
    exit: {
      x: rawExit.x,
      y: rawExit.y,
      source: {
        byteOffset: 6,
        raw: [
          `0x${hex(header[6], 2)}`,
          `0x${hex(header[7], 2)}`
        ]
      }
    },
    time: {
      raw: [
        `0x${hex(header[8], 2)}`,
        `0x${hex(header[9], 2)}`
      ],
      raw16: `0x${hex(timeRaw, 4)}`,
      rawDecimal: timeRaw,
      bcd: time?.value ?? null,
      bcdString: time?.text ?? null,
      bcdValid: time?.valid ?? false
    },
    levelFlags: {
      param1: `0x${hex(header[10], 2)}`,
      param2: `0x${hex(header[11], 2)}`
    },
    memoryFields: flags
  };
}

function decodeBcdBytes(high, low) {
  const hiTens = high >> 4;
  const hiUnits = high & 0x0f;
  const loTens = low >> 4;
  const loUnits = low & 0x0f;
  const valid = [hiTens, hiUnits, loTens, loUnits].every((value) => value < 10);
  if (!valid) return null;
  const value = hiTens * 1000 + hiUnits * 100 + loTens * 10 + loUnits;
  const text = `${hiTens}${hiUnits}:${loTens}${loUnits}`;
  return { valid, value, text };
}

function summarizeTilePositions(tilePositions) {
  return {
    diamonds: tilePositions.diamonds.length,
    rocks: tilePositions.rocks.length,
    walls: tilePositions.walls.length,
    exits: tilePositions.exits.length,
    monsters: tilePositions.monsters.length
  };
}

function renderLevelMetadata(level, renderedLevel, fixturePath) {
  const fixtureBytes = existsSync(fixturePath) ? readFileSync(fixturePath) : null;
  const fixtureSha256 = fixtureBytes ? sha256(fixtureBytes) : null;
  const exitPositions = dedupePositionList(level.tilePositions.exits.concat([{
    x: level.headerMeta.exit.x,
    y: level.headerMeta.exit.y
  }]));
  return {
    levelIndex: level.levelIndex,
    levelNumber: level.levelNumber,
    sourceAddress: level.sourceAddress,
    sourceOffset: level.sourceOffset,
    header: level.header,
    headerMeta: level.headerMeta,
    size: {
      width: level.width,
      height: level.height,
      stride: level.stride,
      logicalCellCount: level.width * level.height
    },
    fixture: {
      png: `${renderedLevel?.png ?? `${LEVEL_OUTPUT_DIR}/mine-level-${String(level.levelNumber).padStart(2, "0")}.png`}`,
      width: level.width * TILE_WIDTH,
      height: level.height * TILE_HEIGHT,
      encodedLength: level.encodedLength,
      sha256: fixtureSha256
    },
    playerStart: {
      x: level.playerStart.x,
      y: level.playerStart.y,
      gridAddress: level.playerStart.gridAddress
    },
    exitsFromHeader: [
      { x: level.headerMeta.exit.x, y: level.headerMeta.exit.y, source: "header", gridAddress: level.exit.gridAddress }
    ],
    positions: {
      diamonds: level.tilePositions.diamonds,
      rocks: level.tilePositions.rocks,
      walls: level.tilePositions.walls,
      exits: exitPositions
    },
    counts: level.counts,
    tileClassification: level.tileClassification,
    checksums: {
      headerSha256: level.headerSha256
    }
  };
}

