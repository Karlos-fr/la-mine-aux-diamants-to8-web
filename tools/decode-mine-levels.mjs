function headerPosition(header, offset) {
  const x = header[offset];
  const y = header[offset + 1];
  return {
    x,
    y,
    gridAddress: hexAddress(GRID_BASE + y * STRIDE + x)
  };
}

function writeDecodedTile(fullGrid, grid, counts, specialMarkers, tilePositions, tileId, x, y) {
  if (x >= WIDTH || y >= HEIGHT) return;
  fullGrid[y][x] = tileId;
  grid[y][x] = tileId;
  const key = `0x${hex(tileId, 2)}`;
  counts[key] = (counts[key] ?? 0) + 1;
  if (tileId === 0x02 || tileId === 0x17) {
    specialMarkers.push({ tileId: key, x, y, gridAddress: hexAddress(GRID_BASE + y * STRIDE + x) });
  }
  if (ROCK_TILE_IDS.includes(tileId)) {
    tilePositions.rocks.push({ x, y, gridAddress: hexAddress(GRID_BASE + y * STRIDE + x) });
  }
  if (DIAMOND_TILE_IDS.includes(tileId)) {
    tilePositions.diamonds.push({ x, y, gridAddress: hexAddress(GRID_BASE + y * STRIDE + x) });
  }
  if (WALL_TILE_IDS.includes(tileId)) {
    tilePositions.walls.push({ x, y, gridAddress: hexAddress(GRID_BASE + y * STRIDE + x) });
  }
  if (EXIT_TILE_IDS.includes(tileId)) {
    tilePositions.exits.push({ x, y, gridAddress: hexAddress(GRID_BASE + y * STRIDE + x) });
  }
  if (SPECIAL_MONSTER_TILE_IDS.includes(tileId)) {
    tilePositions.monsters.push({ x, y, gridAddress: hexAddress(GRID_BASE + y * STRIDE + x) });
  }
}

function aggregate(levels) {
  const counts = {};
  for (const level of levels) {
    for (const [tileId, count] of Object.entries(level.counts)) {
      counts[tileId] = (counts[tileId] ?? 0) + count;
    }
  }
  return sortCounts(counts);
}

function renderLevelImages(memory, levels, outDir) {
  const maxTileId = Math.max(...levels.flatMap((level) => level.grid.flat()));
  const tileImages = [];
  for (let tileId = 0; tileId <= maxTileId; tileId += 1) {
    const address = ATLAS_START + tileId * TILE_SIZE_BYTES;
    tileImages[tileId] = renderTileRgba(memory.subarray(address, address + TILE_SIZE_BYTES));
  }

  return levels.map((level) => {
    const width = level.width * TILE_WIDTH;
    const height = level.height * TILE_HEIGHT;
    const rgba = Buffer.alloc(width * height * 4);
    for (let tileY = 0; tileY < level.height; tileY += 1) {
      for (let tileX = 0; tileX < level.width; tileX += 1) {
        blit(
          rgba,
          width,
          tileImages[level.grid[tileY][tileX]],
          TILE_WIDTH,
          TILE_HEIGHT,
          tileX * TILE_WIDTH,
          tileY * TILE_HEIGHT
        );
      }
    }
    const fileName = `mine-level-${String(level.levelNumber).padStart(2, "0")}.png`;
    writeFileSync(join(outDir, fileName), encodePng(width, height, rgba));
    return {
      levelNumber: level.levelNumber,
      png: `levels/${fileName}`,
      width,
      height
    };
  });
}

function blit(target, targetWidth, source, sourceWidth, sourceHeight, dx, dy) {
  for (let y = 0; y < sourceHeight; y += 1) {
    for (let x = 0; x < sourceWidth; x += 1) {
      const sourceOffset = (y * sourceWidth + x) * 4;
      const targetOffset = ((dy + y) * targetWidth + dx + x) * 4;
      target[targetOffset] = source[sourceOffset];
      target[targetOffset + 1] = source[sourceOffset + 1];
      target[targetOffset + 2] = source[sourceOffset + 2];
      target[targetOffset + 3] = source[sourceOffset + 3];
    }
  }
}

